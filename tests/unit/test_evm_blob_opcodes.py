def test_mainnet_single_opcodes_registered():
    missing = [op for op in MAINNET_OPCODES if not native._evm_opcode_supported_python(op)]
    assert not missing, f"missing opcodes: {[hex(o) for o in missing]}"


def test_blobhash_and_blobbasefee(monkeypatch):
    monkeypatch.setattr(EVM, "_native_pure_available", lambda self: False)
    monkeypatch.setattr(EVM, "_run_native_until_halt", lambda self, bc, jt: None)
    blob_hash = int("ab" * 32, 16)
    ctx = EVMContext(blob_base_fee=9, blob_hashes=[blob_hash])
    evm = EVM(context=ctx)
    # PUSH0 BLOBHASH, PUSH0 BLOBBASEFEE
    result = evm.execute_bytecode(bytes([0x5F, 0x49, 0x5F, 0x4A, 0x00]))
    assert result["stack"][-1] == 9
    assert result["stack"][-2] == 0
    assert result["stack"][-3] == blob_hash
